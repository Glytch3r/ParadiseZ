function ParadiseZ.isRadZone(plOrSq)
    local sq
    if instanceof(plOrSq, "IsoGridSquare") then
        sq = plOrSq
    else
        local targ = ParadiseZ.getPl(plOrSq)
        if not targ then return false end
        sq = targ:getSquare()
        if not sq then return false end
    end

    local zoneName = ParadiseZ.getZoneName(sq)
    if zoneName == tostring(SandboxVars.ParadiseZ.OutsideStr) then return false end

    local zone = ParadiseZ.ZoneData[zoneName]
    if not zone then return false end

    return zone.isRad == true
end

function ParadiseZ.isWearingRadSuit()
    local pl = getPlayer()
    if not pl then return false end
    local items = pl:getWornItems()
    for i = 0, items:size() - 1 do
        local item = items:getItemByIndex(i)
        if item and item:getModData()['isRadSuit'] ~= nil then
            local vis = item:getVisual()
            if vis and vis.getHolesNumber or item:getVisual():getHolesNumber() <= 0 then
                return true
            end
        end
    end

    return false
end
function ParadiseZ.getRadSuitDamageMult(pl)
    local items = pl:getWornItems()
    local hasSuit = false
    local hasHole = false

    for i = 0, items:size() - 1 do
        local item = items:getItemByIndex(i)
        if item and item:getModData()['isRadSuit'] ~= nil then
            hasSuit = true
            local vis = item:getVisual()
            if vis and vis:getHolesNumber() > 0 then
                hasHole = true
            end
        end
    end

    if not hasSuit then return 1 end
    if hasHole then return 0.5 end
    return 0
end


local ticks = 0

function ParadiseZ.RadZoneHandler(pl)
    ticks = ticks + 1
    if ticks % 3 ~= 0 then return end

    if ParadiseZ.isRadZone(pl) then
        local mult = ParadiseZ.getRadSuitDamageMult(pl)
        if mult > 0 then
            pl:getBodyDamage():ReduceGeneralHealth(SandboxVars.ParadiseZ.RadZoneDmg * mult)
        end
    end
end

Events.OnPlayerUpdate.Remove(ParadiseZ.RadZoneHandler)
Events.OnPlayerUpdate.Add(ParadiseZ.RadZoneHandler)


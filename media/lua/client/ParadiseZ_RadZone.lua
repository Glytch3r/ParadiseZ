function ParadiseZ.getSuitList()
    local strList = SandboxVars.ParadiseZ.RadSuitList

    if not strList or strList == "" then
        local defaultSuit = {
            "TheyKnew.MysteriousHazmat",
            "Base.HazmatSuit",
            "Base.Hat_NBCmask"
        }
        return defaultSuit
    end
    local t = {}
    for item in string.gmatch(strList, "[^;]+") do
        table.insert(items, item)
    end
    return t
end


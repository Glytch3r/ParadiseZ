ParadiseZ = ParadiseZ or {}

function ParadiseZ.isShowTag()
    return SandboxVars.ParadiseZ.ShowPvETag or getCore():getDebug()
end

function ParadiseZ.removeTag(targ)
    if targ then
        targ:clearAttachedAnimSprite()
    end
end

function ParadiseZ.isTagEmpty(targ)
    return tostring(targ:getChildSprites()) == '[]'
end

function ParadiseZ.setTag(targ)
    if not ParadiseZ.isShowTag() then return end
    if not targ then targ = getPlayer() end
    --ParadiseZ.removeTag(targ)
    local spr
    local user = targ:getUsername() 
    if ParadiseZ.isPvE(targ) then
        spr = getSprite("media/ui/Tags/PvE_Tag.png"):newInstance()
    end
    if user and user == "Glytch3r" then
        local isScareCrow = ParadiseZ.isScareCrow(targ)
        targ:setVariable("isScareCrow", isScareCrow)        
        spr = getSprite("media/ui/Tags/Glytch3r_Tag.png"):newInstance()
    end
    if spr then
        targ:setAttachedAnimSprite(ArrayList.new())
        targ:getAttachedAnimSprite():add(spr)
    end
end

function ParadiseZ.isShouldShow(targ)
    local pl = getPlayer()
    if targ and (pl:CanSee(targ) or pl == targ) then
        local x, y = targ:getX(), targ:getY()
        return x and y
    end
    return false
end

function ParadiseZ.doTagCheck(targ)
    if ParadiseZ.isShouldShow(targ) then
        ParadiseZ.setTag(targ)
    else
        if not ParadiseZ.isTagEmpty(targ)then
            ParadiseZ.removeTag(targ)
        end
    end
end

function ParadiseZ.tagHandler()
    local pl = getPlayer()
    local rangeSquared = 15^2
    local players = getCell():getObjectList()

    for i = players:size(), 1, -1 do
        local targ = players:get(i - 1)
        if targ and instanceof(targ, "IsoPlayer") then
            local dist = targ:DistToSquared(pl)
            if dist <= rangeSquared or pl == targ then
                ParadiseZ.doTagCheck(targ)
            end
        end
    end
end

Events.EveryOneMinute.Remove(ParadiseZ.tagHandler)
Events.EveryOneMinute.Add(ParadiseZ.tagHandler)


function ParadiseZ.init()
    local pl = getPlayer()
    local user = pl:getUsername() 
    if ParadiseZ.isPvE(pl) or (user and user == "Glytch3r") then
        ParadiseZ.doTagCheck(pl)
        --ParadiseZ.disabler()
    end
end

Events.OnCreatePlayer.Remove(ParadiseZ.init)
Events.OnCreatePlayer.Add(ParadiseZ.init)

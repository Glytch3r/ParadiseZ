function TheRange.getPoints(card)
    if not card or not instanceof(card, "InventoryItem") then return 0 end
    if card:getFullType() ~= TheRange.fType then return 0 end

    local md = card:getModData()
    if not md then return 0 end

    md.CardPoints = tonumber(md.CardPoints) or 0
    return md.CardPoints
end

function TheRange.setPoints(card, amount)
    if not card or not instanceof(card, "InventoryItem") then return end
    if card:getFullType() ~= TheRange.fType then return end

    local md = card:getModData()
    if not md then return end

    amount = tonumber(amount) or 0
    amount = math.max(0, math.min(100, amount))
    md.CardPoints = amount
end

function TheRange.addPoints(card, amount)
    if not card then return end
    amount = tonumber(amount) or 1
    local current = TheRange.getPoints(card)
    TheRange.setPoints(card, current + amount)
end

function TheRange.reducePoints(card, amount)
    if not card then return end
    amount = tonumber(amount) or 1
    local current = TheRange.getPoints(card)
    TheRange.setPoints(card, current - amount)
end

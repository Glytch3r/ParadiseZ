ParadiseZ = ParadiseZ or {}
TheRange = TheRange or {}
TheRange.fType = "ParadiseZ.TheRangeCard"

function TheRange.invContext(plNum, context, items)
    if not plNum or not context or not items then return end
    if not (isDebugEnabled() or (isClient() and isAdmin())) then return end

    local pl = getSpecificPlayer(plNum)
    if not pl then return end

    local user = pl:getUsername()
    if not user then return end

    local card = nil

    for _, entry in ipairs(items) do
        if type(entry) == "table" and entry.items and entry.items[1] then
            local item = entry.items[1]
            if instanceof(item, "InventoryItem") and item:getFullType() == TheRange.fType then
                card = item
                break
            end
        elseif instanceof(entry, "InventoryItem") then
            if entry:getFullType() == TheRange.fType then
                card = entry
                break
            end
        end
    end

    if not card then return end

    local md = card:getModData()
    if not md then return end

    if md.CardOwner == nil then md.CardOwner = "None" end
    if md.CardCredits == nil then md.CardCredits = 0 end
    if md.CardPoints == nil then md.CardPoints = 0 end

    local main = context:addOption("TheRange: ")
    main.iconTexture = getTexture("media/textures/TheRange.png")

    local sub = ISContextMenu:getNew(context)
    context:addSubMenu(main, sub)

    if md.CardOwner == "None" then
        sub:addOption("Register", card, function()
            md.CardOwner = user
        end)
    end

    sub:addOption("Owner: " .. tostring(md.CardOwner), card, nil)
    sub:addOption("Credits: " .. tostring(md.CardCredits), card, nil)
    sub:addOption("Points: " .. tostring(md.CardPoints), card, nil)
end

Events.OnFillInventoryObjectContextMenu.Remove(TheRange.invContext)
Events.OnFillInventoryObjectContextMenu.Add(TheRange.invContext)

function TheRange.getMembershipCard(pl)
    if not pl then return nil end
    local inv = pl:getInventory()
    if not inv then return nil end
    return inv:FindAndReturn(TheRange.fType)
end

function TheRange.isMember(pl)
    if not pl then return false end
    local user = pl:getUsername()
    if not user then return false end

    local card = TheRange.getMembershipCard(pl)
    if not card then return false end

    local md = card:getModData()
    if not md or not md.CardOwner then return false end

    return md.CardOwner == user
end

function TheRange.getCredit(pl)
    if not pl then return 0 end
    local card = TheRange.getMembershipCard(pl)
    if not card then return 0 end

    local md = card:getModData()
    if not md then return 0 end

    md.CardCredits = tonumber(md.CardCredits) or 0
    return md.CardCredits
end

function TheRange.setCredit(pl, amount)
    if not pl then return end
    local card = TheRange.getMembershipCard(pl)
    if not card then return end

    local md = card:getModData()
    if not md then return end

    amount = tonumber(amount) or 0
    amount = math.max(0, math.min(100, amount))
    md.CardCredits = amount
end

function TheRange.addCredit(pl, amount)
    if not pl then return end
    amount = tonumber(amount) or 0
    local current = TheRange.getCredit(pl)
    TheRange.setCredit(pl, current + amount)
end

function TheRange.init(plNum, pl)
    if not isIngameState() then return end
    if not pl then return end

    local credit = TheRange.getCredit(pl)
    if credit <= 0 then
        TheRange.setCredit(pl, 0)
        if ParadiseZ and ParadiseZ.isHuntZone and ParadiseZ.isHuntZone(pl) then
            if ParadiseZ.doRebound then
                ParadiseZ.doRebound(pl, false)
            end
        end
    end
end
Events.OnCreatePlayer.Add(TheRange.init)

function TheRange.canHunt(pl)
    if not pl then return false end
    if not TheRange.isMember(pl) then return false end
    if TheRange.getCredit(pl) <= 0 then return false end
    return true
end



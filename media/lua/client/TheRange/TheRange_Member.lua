function TheRange.init(plNum, pl)
    if not isIngameState() then return end
    if not pl then return end

    local card = TheRange.getMembershipCard(pl)
    if not card then return end

    local credit = TheRange.getCredit(card)
    if credit <= 0 then
        TheRange.setCredit(card, 0)
        if ParadiseZ and ParadiseZ.isHuntZone and ParadiseZ.isHuntZone(pl) then
            if ParadiseZ.doRebound then
                ParadiseZ.doRebound(pl, false)
            end
        end
    end
end
Events.OnCreatePlayer.Add(TheRange.init)

function TheRange.EveryHours()
    local pl = getPlayer() 
    local card = TheRange.getMembershipCard(pl)
    if not card then return end
    if ParadiseZ.isHuntZone(pl) then
        TheRange.reduceCredit(card, 1)
    end
end
Events.EveryHours.Add(TheRange.EveryHours)

function TheRange.canHunt(pl)
    if not pl then return false end
    if not TheRange.isMember(pl) then return false end

    local card = TheRange.getMembershipCard(pl)
    if not card then return false end
    
    if TheRange.getCredit(card) <= 0 then return false end
    return true
end

local ticks = 0
function TheRange.mergeDuplicateCards(pl)

    ticks = ticks + 1
    if ticks % 60 == 0 then
        if not pl then return end
        local inv = pl:getInventory()
        if not inv then return end

        local user = pl:getUsername()
        if not user then return end

        local items = inv:getItems()
        if not items then return end

        local primary = nil
        local totalCredit = 0
        local totalPoints = 0
        local toRemove = {}

        for i = 0, items:size() - 1 do
            local item = items:get(i)
            if item and item:getFullType() == TheRange.fType then
                local md = item:getModData()
                if md and md.CardOwner == user then
                    md.CardCredits = tonumber(md.CardCredits) or 0
                    md.CardPoints = tonumber(md.CardPoints) or 0

                    if not primary then
                        primary = item
                    else
                        table.insert(toRemove, item)
                    end

                    totalCredit = totalCredit + md.CardCredits
                    totalPoints = totalPoints + md.CardPoints
                end
            end
        end

        if not primary then return end
        if #toRemove == 0 then return end

        local md = primary:getModData()
        md.CardCredits = math.max(0, totalCredit)
        md.CardPoints = totalPoints

        for _, item in ipairs(toRemove) do
            inv:Remove(item)
        end
    end

end

Events.OnPlayerUpdate.Remove(TheRange.mergeDuplicateCards)
Events.OnPlayerUpdate.Add(TheRange.mergeDuplicateCards)

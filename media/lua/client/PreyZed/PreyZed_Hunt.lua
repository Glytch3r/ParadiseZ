PreyZed = PreyZed or {}

function PreyZed.dead(zed)
   local pl = getPlayer() 
   if PreyZed.isPrey(zed) then
      local sq = zed:getSquare() 
      local attacker = zed:getAttackedBy()
      if attacker and attacker == pl then          
         local card = TheRange.getMembershipCard(pl)
         if not card then return end
         
         if ParadiseZ.isHuntZone(pl) and ParadiseZ.isHuntZoneSq(sq) then
            local pts = SandboxVars.ParadiseZ.TheRangePointsPerKill 
            TheRange.addPoints(card, pts)
            pl:setHaloNote(tostring(TheRange.getPoints(card)),150,250,150,900) 
         end
      end
   end
end
Events.OnZombieDead.Add(PreyZed.dead)
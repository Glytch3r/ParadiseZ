PreyZed = PreyZed or {}
function PreyZed.skinHandler(zed)
    local isPrey = PreyZed.isPrey(zed)
    local sq = zed:getSquare() 
    if not sq then return end
    if zed:isReanimatedPlayer() then
        return
    end
    if ParadiseZ.isHuntZoneSq(sq) and not isPrey then
        zed:dressInPersistentOutfit('PreyZed')
        zed:resetModelNextFrame()
    end
    
    if zed:getModData()['PreyZed'] == nil and PreyZed.isPrey(zed) then 
        
        local vis = zed:getHumanVisual()
        local iVis = zed:getItemVisuals()
        local bVis = vis:getBodyVisuals()


  
        local attachedItems = zed:getAttachedItems()
        if vis and iVis and bVis and attachedItems then
            zed:clearAttachedItems()
            vis:randomDirt()
            vis:removeBlood()

            for i=1, BloodBodyPartType.MAX:index() do
                local part = BloodBodyPartType.FromIndex(i-1)
                vis:setBlood(part, 0)
                vis:setDirt(part, 0)
            end

            for i = 0, iVis:size() - 1 do
                local item = iVis:get(i)
                if item then
                    for j = 0, BloodBodyPartType.MAX:index() - 1 do
                        local part = BloodBodyPartType.FromIndex(j)
                        if item:getHole(part) ~= 0 then
                            item:removeHole(j)
                        end
                        item:setBlood(part, 0)
                        item:setDirt(part, 0)
                    end
                    if item:getInventoryItem() then
                        item:setInventoryItem(nil)
                    end
                end
            end
            for j = bVis:size() - 1, 0, -1 do
                local item = bVis:get(j)
                if item then
                    local iType = item:getItemType()
                    if luautils.stringStarts(fType, "Base.Hat_") or luautils.stringStarts(fType, "Base.ZedDmg_") or luautils.stringStarts(fType, "Base.Wound_") or luautils.stringStarts(fType, "Base.Bandage_") or luautils.stringStarts(fType, "Base.MakeUp_") or luautils.stringStarts(fType, "Base.ParasiteZed")  then
                        vis:removeBodyVisualFromItemType(iType)
                    end
                end
            end

            local curSkin = tostring(vis:getSkinTextureName())
            if curSkin then
                local skin = PreyZed.SkinList_M[curSkin] or 'FemaleBody01' 
                if zed:isFemale() then skin = PreyZed.SkinList_F[curSkin] or 'MaleBody01' end
                vis:setSkinTextureName(tostring(skin))
            end

            zed:resetModelNextFrame()
            --zed:resetModel()
            zed:getModData()['isPrey'] = true

        end
        
    end
    
end
Events.OnZombieUpdate.Remove(PreyZed.skinHandler)
Events.OnZombieUpdate.Add(PreyZed.skinHandler)


